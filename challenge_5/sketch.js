
function setup() {
	// create a place to draw
	createCanvas(640, 480);
	noStroke();
	// noLoop();
}

function draw() {
	// clear the background
	background(50, 20, 21);

	// set a fill color
	fill(255, 255, 255);
	ellipse(mouseX, mouseY, 50, 50);
	fill(28,117, 255);
	ellipse(mouseX, mouseY, 30, 30);
}

	
